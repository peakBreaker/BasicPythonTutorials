# basic conditions

myvar=4

if myvar == 4:
    print("myvar is 4!")

if myvar < 10:
    print("myvar is smaller than 10")

# Can also use > (bigger than)

if myvar < 4:
    print("myvar is smaller than 4!")
else:
    print("myvar is bigger than 4!")

if myvar >= 4:
    print("myvar is bigger than or equal to 4!")
else:
    print("myvar is smaller than 4")
