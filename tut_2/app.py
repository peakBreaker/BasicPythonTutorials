# basic conditions

myvar=4

if myvar == 4:
    print("%i is 4!" % myvar)

if myvar < 10:
    print("%i is smaller than 10" % myvar)

# Can also use > (bigger than)

if myvar < 4:
    print("%i is smaller than 4!" % myvar)
else:
    print("%i is bigger than 4!" % myvar)

if myvar >= 4:
    print("%i is bigger than or equal to 4!" % myvar)
else:
    print("%i is smaller than 4" % myvar)
